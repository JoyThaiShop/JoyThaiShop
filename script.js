        header.innerHTML = `<img src="${imgUrl}" alt="${cat}" style="width:36px; height:36px; border-radius:50%; object-fit:cover; border:1.5px solid var(--border2); display:block; flex-shrink:0;"> ${getCatName(cat)}`;















        grid.appendChild(header);































